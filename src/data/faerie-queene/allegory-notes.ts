/**
 * The Faerie Queene — allegory-note data model and seed entries.
 *
 * This is Spenser's signature pedagogical contribution to the Tome catalog:
 * no other book in the library teaches *how allegorical reading works*,
 * and The Faerie Queene is the canonical example in the English tradition.
 *
 * The allegory-note is a distinct annotation subtype that explicitly
 * presents *multiple simultaneous readings* of a figure, passage, or
 * place — moral, theological, political, typological, personal — rather
 * than collapsing to a single interpretive claim. The data shape honors
 * the doubleness Spenser wrote in: every figure is simultaneously a
 * person in a story AND a figure for a virtue, vice, Church, historical
 * personage, theological concept. Neither reading cancels the other.
 *
 * Rendering (reader component, Part 3B integration):
 *   - Shows the `literalNote` first (what happens at the plot level).
 *   - Expands to show the `readings[]` as layered panels, each typed
 *     (moral / theological / political / typological / personal).
 *   - Authorities cited (where present) are scholarly, verified — never
 *     fabricated. Spec: "Do not cite Greenblatt, Hamilton, Nohrnberg,
 *     Berger, Teskey, or any other specific critic without verifying
 *     the citation."
 *
 * Target density (per Part 4):
 *   - Books I–II: 2–4 allegory notes per canto (cleanest allegory)
 *   - Books III–VI: 1–3 per canto
 *   - Mutabilitie Cantos: 2–3 per canto (densely philosophical, brief)
 *   ≈ 120–180 allegory notes across the whole poem.
 *
 * Seed entries below cover Book I canto i — the demo-critical opening —
 * as templates for the Part 4 annotation pass. Additional notes are
 * generated by the Part 4 pipeline (Opus for the ten named clusters,
 * Sonnet for the rest), patterned on these seeds.
 */

import type { FaerieQueeneSpeaker } from "./speakers"

/** Anchor into the poem's four-level structural hierarchy. */
export interface AnchorRef {
  book: number          // 1–6, or 7 for Mutabilitie
  canto: number         // 1–12 (or 6/7 for Mutabilitie)
  stanza?: number       // 1-based; absent = canto-level note
  line?: number         // 1–9; absent = stanza-level note
  /** Optional range end; if present, the note spans [stanza.line, endStanza.endLine]. */
  endStanza?: number
  endLine?: number
}

/** Types of allegorical reading. A note usually carries several. */
export type AllegoryReadingType =
  | "moral"         // virtue/vice personification — what the figure teaches
  | "theological"   // Christian doctrine — grace, sin, faith, works
  | "political"     // Elizabethan politics — the queen, Ireland, Mary, Spain
  | "typological"   // Old-Testament-prefigures-New figuration; Revelation
  | "personal"      // Spenser's own biography; Ralegh, Grey, Ireland
  | "literary"      // acknowledged inheritance (Ariosto, Virgil, Ovid, Chaucer, Bible)

export interface AllegoryReading {
  type: AllegoryReadingType
  /** 1–3 sentences of interpretation. Honest about debate where debate exists. */
  reading: string
  /** Optional scholarly authority. MUST be verifiable — do not fabricate. */
  authority?: string
}

export interface AllegoryNote {
  id: string
  anchor: AnchorRef
  /** The figure, place, or passage being read — a short descriptive label. */
  subject: string
  /** Optional speaker id(s) the note concerns (keys into FAERIE_QUEENE_SPEAKERS). */
  speakerIds?: string[]
  /** What happens at the plot level — the story-as-story. 1–2 sentences. */
  literalNote: string
  /** The layered readings. Order is conventional: moral → theological → typological → political → personal → literary. */
  readings: AllegoryReading[]
  /** Optional cross-references to passages elsewhere in Spenser, or to other books in the catalog. */
  crossReferences?: Array<{
    bookId: string       // tome book id, e.g. "orlando-furioso", "paradise-lost"
    anchor?: string      // e.g. "canto-1 stanza-42" or "book-4 line-268"
    note: string
  }>
}

