/**
 * Group seed entries by (book, canto) for the reader component's drawer.
 */
export function allegoryNotesForCanto(book: number, canto: number): AllegoryNote[] {
  return ALLEGORY_NOTE_SEEDS.filter(
    (n) => n.anchor.book === book && n.anchor.canto === canto,
  )
}

/** Total count (used by verification gate Part 9 step 7). */
export const ALLEGORY_NOTE_COUNT = ALLEGORY_NOTE_SEEDS.length

/** Speaker-id validator — ensures any referenced speaker exists in the palette. */
export function validateSpeakerReferences(
  notes: AllegoryNote[],
  validIds: Set<string>,
): { valid: boolean; missing: string[] } {
  const missing: string[] = []
  for (const n of notes) {
    for (const id of n.speakerIds ?? []) {
      if (!validIds.has(id)) missing.push(`${n.id}: ${id}`)
    }
  }
  return { valid: missing.length === 0, missing }
}

