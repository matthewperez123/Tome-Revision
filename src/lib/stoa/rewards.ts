// ── Assembly (seed + registry) ───────────────

function buildReward(slug: BookSlug): StoaReward {
  const book = BOOK_EXPERIENCES[slug]
  const seed = STOA_SEEDS[slug]
  return {
    id: slug,
    bookSlug: slug,
    bookId: book.bookId,
    bookTitle: book.title,
    author: book.author,
    sealName: book.sealName,
    provenanceLabel: STOA_PROVENANCE_LABEL,
    artSrc: `${STOA_ART_DIR}/${slug}.svg`,
    altText: `Original Tome Stoa painting for ${book.title}: ${seed.title}, ` +
      `an arch-and-medallion composition in the book's palette. ${seed.description}`,
    ...seed,
  }
}

/** The twelve Stoa rewards, keyed by world slug (1:1 with books). */
export const STOA_REWARDS: Record<BookSlug, StoaReward> = Object.fromEntries(
  (Object.keys(STOA_SEEDS) as BookSlug[]).map((slug) => [slug, buildReward(slug)]),
) as Record<BookSlug, StoaReward>

// ── Accessors ────────────────────────────────

/** All rewards in registry order. */
export function listStoaRewards(): StoaReward[] {
  return (Object.keys(BOOK_EXPERIENCES) as BookSlug[]).map((slug) => STOA_REWARDS[slug])
}

/** Look up a reward by id (world slug). Returns undefined for unknown ids. */
export function getStoaReward(id: string): StoaReward | undefined {
  return id in STOA_REWARDS ? STOA_REWARDS[id as BookSlug] : undefined
}

/** Type guard for reward ids. */
export function isStoaRewardId(value: string): value is BookSlug {
  return value in STOA_REWARDS
}

export const STOA_REWARD_COUNT = listStoaRewards().length
