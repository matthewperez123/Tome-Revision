// Twelve seeded Stoa rewards, one per featured book experience
// (MASTER_EXECUTION_PLAN §11). Mirrors the stoa_paintings model
// (docs/architecture/supabase-data-map.md §2.3): 1:1 painting ↔ book,
// unlock derived from quiz_results.passed for that book. This module is
// the static catalog; per-user restored state for the showcase lives in
// ./state.ts (localStorage, no Supabase writes).
//
// Every passage is a short, real, public-domain excerpt from the book
// itself. All artwork is original Tome art (scripts/stoa/gen-stoa-art.mjs)
// — never historical source material.

