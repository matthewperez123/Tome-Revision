// ── Types ────────────────────────────────────

export interface StoaPassage {
  /** Short verbatim excerpt (public domain). */
  text: string
  /** Where it lives in the book, incl. translation when relevant. */
  citation: string
}

export interface StoaReward {
  /** Stable reward id — matches the book's world slug, 1:1 with the book. */
  id: BookSlug
  bookSlug: BookSlug
  /** Canonical catalog book ID (books.id / stoa_paintings.unlocking_book_id). */
  bookId: string
  title: string
  bookTitle: string
  author: string
  /** What the restored painting depicts (curatorial wall label). */
  description: string
  /** Related public-domain passage from the book. */
  passage: StoaPassage
  /** One line of Virgil commentary, in his voice. */
  virgilLine: string
  provenanceLabel: typeof STOA_PROVENANCE_LABEL
  /** Human-readable provenance/licensing note (rendered in detail view). */
  provenanceNote: string
  /** Copy for the EMPTY niche (locked) state. */
  emptyCopy: { heading: string; body: string }
  /** Copy for the RESTORED (unlocked) state. */
  restoredCopy: { heading: string; body: string }
  /** Painting asset (original Tome art). */
  artSrc: string
  altText: string
  /** Seal medallion earned alongside this reward (registry sealName). */
  sealName: string
}

